    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.menu_main, menu);
        menu.findItem(R.id.bluetooth_search).setVisible(false);
        menu.findItem(R.id.bluetooth_status).setVisible(false);
        if(menu.findItem(R.id.aws_resume).isVisible()){
            menu.findItem(R.id.aws_pause).setVisible(true);
            menu.findItem(R.id.aws_resume).setVisible(false);
        }
        if(!menu.findItem(R.id.aws_resume).isVisible()) {
            menu.findItem(R.id.aws_pause).setVisible(false);
            menu.findItem(R.id.aws_resume).setVisible(true);
        }
        menu.findItem(R.id.aws_stop).setVisible(true);
        menu.findItem(R.id.aws_upload).setVisible(true);

        return true;
    }

